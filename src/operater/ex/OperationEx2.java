package operater.ex;

public class OperationEx2 {
    public static void main(String[] args) {
        // 변수를 선언하고 합과 평균을 구하는 프로그램
        double val1 = 1.5;
        double val2 = 2.5;
        double val3 = 3.5;

        double sum = val1 + val2 + val3;
        double average = sum / 3;

        System.out.println("sum = " + sum);
        System.out.println("average = " + average);

    }
}
